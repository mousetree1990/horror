function ib1(){
    let x = document.getElementById("potions");
    let checkWeaponVis = document.getElementById("weapons");
    let checkArmorVis = document.getElementById("armor");
    let checkKeyVis = document.getElementById("keyItems");
    let checkOptionVis = document.getElementById("options");
    let checkInventoryVis = document.getElementById("inventory");
    if (x.style.display === "flex"){
       x.style.display = "none";
    } else {
       x.style.display = "flex";
    }
    if (checkWeaponVis.style.display === "flex"){
       checkWeaponVis.style.display = "none";
    }
    if (checkArmorVis.style.display === "flex"){
       checkArmorVis.style.display = "none";
    }
    if (checkKeyVis.style.display === "flex"){
       checkKeyVis.style.display = "none";
    }
    if (checkOptionVis.style.display === "flex"){
       checkOptionVis.style.display = "none";
    }
    if (checkInventoryVis.style.display === "none"){
        checkInventoryVis.style.display = "flex";
    } else {
        checkInventoryVis.style.display = "flex";
    }
    if (x.style.display === "none"){
        checkInventoryVis.style.display = "none";
    }
    }
   
